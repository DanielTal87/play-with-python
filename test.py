def add_numbers(a, b):
    return a + b

def multiply_numbers(a, b):
    return a * b

def greet(name):
    return f"Hello, {name}!"

# Testing the functions
result_sum = add_numbers(5, 3)
result_product = multiply_numbers(4, 7)
greeting = greet("John")

# Displaying the results
print(f"Sum: {result_sum}")
print(f"Product: {result_product}")
print(greeting)
